package Controlstatements;


public class Test55 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		for(int i=3;i>=0;i--)
		{
			System.out.println("it is "+i);
		}
		for(int i=3;i>=0;)
		{
			System.out.println("it is "+i);
		}
		for(int i=3;;i--)
		{
			System.out.println("it is "+i);
		}
	}

}
